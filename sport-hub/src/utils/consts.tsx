/**
 * Returns a random image URL from the specified S3_IMAGES group.
 * When no group is provided, selects from all groups combined.
 */
export function randomS3Image(group?: keyof typeof S3_IMAGES): string {
  const pool = group ? S3_IMAGES[group] : Object.values(S3_IMAGES).flat();
  return pool[Math.floor(Math.random() * pool.length)];
}

/** Maps discipline enumValues to the closest S3_IMAGES group. */
const DISCIPLINE_ENUM_TO_IMAGE_GROUP: Record<number, keyof typeof S3_IMAGES> = {
  1: 'TRICKLINE',           // TRICKLINE
  2: 'TRICKLINE',           // TRICKLINE_AERIAL
  3: 'TRICKLINE',           // TRICKLINE_JIB_AND_STATIC
  4: 'TRICKLINE',           // TRICKLINE_TRANSFER
  5: 'FREESTYLE_HIGHLINE',  // FREESTYLE_HIGHLINE
  6: 'SPEED_HIGHLINE',      // SPEED (generic)
  7: 'SPEED_SHORT',         // SPEED_SHORT
  8: 'SPEED_HIGHLINE',      // SPEED_HIGHLINE
  11: 'RIGGING',            // RIGGING
  12: 'FREESTYLE_HIGHLINE', // FREESTYLE
};

/**
 * Returns a random hero image appropriate for the given discipline enum value
 * (the numeric string passed as the `discipline` search param on /rankings).
 * Falls back to the RANKINGS pool when no specific group is available.
 */
export function randomS3ImageForDiscipline(disciplineEnum?: string): string {
  const group = disciplineEnum
    ? DISCIPLINE_ENUM_TO_IMAGE_GROUP[Number(disciplineEnum)]
    : undefined;
  return randomS3Image(group ?? 'RANKINGS');
}


